"""
CLI Entrypoint for Synthetic Data Generation Pipeline.

This script orchestrates the complete Phase 1C pipeline:
1. Load and sample CUAD clauses
2. Generate synthetic risk assessments via Claude API
3. Filter for quality
4. Create human anchor set (30 examples for evaluation)
5. Split into train/validation/test sets

Usage:
    python scripts/generate_synthetic.py --api-key YOUR_KEY
    python scripts/generate_synthetic.py --api-key YOUR_KEY --resume
    python scripts/generate_synthetic.py --filter-only
    python scripts/generate_synthetic.py --api-key YOUR_KEY --dry-run

CRITICAL: Never commit your API key. Use environment variables or .env files.
"""

